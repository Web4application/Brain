import torch
import torch.nn as nn

class ANNModel(nn.Module):
    def __init__(self):
        super().__init__()
        self.fc1 = nn.Linear(361, 128)
        self.relu = nn.ReLU()
        self.fc2 = nn.Linear(128, 4)  # FORWARD, LEFT, RIGHT, STOP

    def forward(self, x):
        x = self.relu(self.fc1(x))
        return self.fc2(x)
